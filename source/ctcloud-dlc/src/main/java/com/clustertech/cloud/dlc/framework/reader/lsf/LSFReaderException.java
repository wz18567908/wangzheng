package com.clustertech.cloud.dlc.framework.reader.lsf;

public class LSFReaderException extends Throwable {
    private static final long serialVersionUID = 6562331769755182856L;

    public LSFReaderException() {
        super();
    }

    public LSFReaderException(String message) {
        super(message);
    }

    public LSFReaderException(Throwable cause) {
        super(cause);
    }

    public LSFReaderException(String message, Throwable cause) {
        super(message, cause);
    }
}
